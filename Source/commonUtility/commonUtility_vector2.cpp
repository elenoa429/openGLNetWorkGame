//==============================================================================
// 関数名 : VECTOR2()
// 引数   : void
// 戻り値 : void
// 説明   : デフォルトコンストラクタ
//==============================================================================
VECTOR2::VECTOR2()
{
	x = 0.0f;
	y = 0.0f;
}

//==============================================================================
// 関数名 : VECTOR2( float x , float y )
// 引数   : void
// 戻り値 : void
// 説明   : 引数付きコンストラクタ
//==============================================================================
VECTOR2::VECTOR2( float x , float y )
{
	this -> x = x;
	this -> y = y;
}

//==============================================================================
// 関数名 : ~VECTOR2()
// 引数   : void
// 戻り値 : void
// 説明   : デストラクタ
//==============================================================================
VECTOR2::~VECTOR2()
{
}

//==============================================================================
// 関数名 : VECTOR2 operator -( VECTOR2 v )
// 引数   : void
// 戻り値 : void
// 説明   : オペレーター : ベクトル同士の減算
//==============================================================================
VECTOR2 VECTOR2::operator -( VECTOR2 v )
{
	VECTOR2 dv;

	dv.x = x - v.x;
	dv.y = y - v.y;

	return dv;
}
