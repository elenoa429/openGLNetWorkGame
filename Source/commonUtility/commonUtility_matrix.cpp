//==============================================================================
// 関数名 : MATRIX operator *( MATRIX mat )
// 引数   : MATRIX* mat
// 戻り値 : void
// 説明   : オペレーター : 行列積用
//==============================================================================
MATRIX MATRIX::operator *( MATRIX mat )
{
	MATRIX mtxMulti;

	MatrixMultiply( &mtxMulti , this , &mat );

	return mtxMulti;
}
