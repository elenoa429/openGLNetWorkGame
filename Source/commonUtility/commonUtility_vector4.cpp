//==============================================================================
// 関数名 : VECTOR4()
// 引数   : void
// 戻り値 : void
// 説明   : デフォルトコンストラクタ
//==============================================================================
VECTOR4::VECTOR4()
{
	x = 0.0f;
	y = 0.0f;
	z = 0.0f;
	w = 0.0f;
}

//==============================================================================
// 関数名 : VECTOR4( float x , float y , float z , float w )
// 引数   : float x , float y , float z , float w : それぞれの軸成分
// 戻り値 : void
// 説明   : 引数付きコンストラクタ
//==============================================================================
VECTOR4::VECTOR4( float x , float y , float z , float w )
{
	this->x = x;
	this->y = y;
	this->z = z;
	this->w = w;
}

