// 戻り値 : CWall*型
// 説明   : 生成処理
//==============================================================================
CWall* CWall::Create()
{
    CWall* pNewInstance = new CWall;


    if (pNewInstance->Init() == false)
    {
        pNewInstance->Release();
        pNewInstance = NULL;
    }


    return pNewInstance;
}


