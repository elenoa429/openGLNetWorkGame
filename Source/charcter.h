class CCharcter : public CScene
{
	//---------------------------------------------
	// [ 列挙型 ]
	//---------------------------------------------
public:
	enum ACTION_STATE
	{
		ACTION_NONE = 0,
		ACTION_MOVE,
		ACTION_ATTACK,
		ACTION_DEAD,
	};

	enum HELTH_STATE
	{
		HELTH_NONE = 0,
		HELTH_DEAD,
	};

	enum CHARCTER_TYPE
	{
		CHARCTER_TYPE_RABBITS = 0,
		CHARCTER_TYPE_CAT,
		CHARCTER_TYPE_PANDA,
		CHARCTER_TYPE_PENGUIN,
		CHARCTER_TYPE_MAX,
	};

	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	CCharcter();
	virtual ~CCharcter();

	virtual bool Init( CHARCTER_TYPE type );
	virtual void Uninit();
	virtual void Update();
	virtual void Draw();

	void SetId( int id ){ m_id = id; }

	int GetId(){ return m_id; }

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
protected:
	VECTOR3 m_pos;
	VECTOR3 m_rot;
	VECTOR3 m_scl;

	CHARCTER_TYPE m_type;						// プレイヤーの種類。見た目を決めるもの。
	int m_id;									// プレイヤーID。〜Pのこと。
	int m_life;									// ライフ。残機のこと。

	ACTION_STATE m_actionState;					// 行動状態識別子
	HELTH_STATE m_helthState;					// ステータス状態識別子

	MATRIX m_mtxWorld;							// ワールドマトリクス
	CParentModelManagerGL* m_pModelManager;		// モデルマネージャへのポインタ
	CMotionGL* m_pMotion;						// モーション処理へのポインタ
};

