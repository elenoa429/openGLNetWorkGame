#include "player.h"

//==============================================================================
// 関数名 : CPlayer()
// 引数   : void
// 戻り値 : void
// 説明   : コンストラクタ
//==============================================================================
CPlayer::CPlayer()
{
}

//==============================================================================
// 関数名 : ~CPlayer()
// 引数   : void
// 戻り値 : void
// 説明   : デフォルトコンストラクタ
//==============================================================================
CPlayer::~CPlayer()
{
}

//==============================================================================
// 関数名 : bool Init(void)
// 引数   : char* modelFileName ：　モデルファイル名
// 戻り値 : 成功したらtrueを返す
// 説明   : 初期化処理
//==============================================================================
bool CPlayer::Init(char* modelFileName)
{
	CSceneGLModel::Init(modelFileName);
	return true;
}

//==============================================================================
// 関数名 : void Uninit(void)
// 引数   : void
// 戻り値 : void
// 説明   : 終了処理
//==============================================================================
void CPlayer::Uninit(void)
{
	//テクスチャ開放
	if (m_pTexture != NULL)
	{
		m_pTexture->Release();
		delete m_pTexture;
		m_pTexture = NULL;
	}

	//マテリアル開放
	if (m_pMat != NULL)
	{
		m_pMat->Release();
		m_pMat = NULL;
	}
}

//==============================================================================
// 関数名 : void Update(void)
// 引数   : void
// 戻り値 : void
// 説明   : 更新処理
//==============================================================================
void CPlayer::Update(void)
{
	CSceneGLModel::Update();
}

//==============================================================================
// 関数名 : void Draw(void)
// 引数   : void
// 戻り値 : void
// 説明   : 描画処理
//==============================================================================
void CPlayer::Draw(void)
{
	CSceneGLModel::Draw();
}

//==============================================================================
// 関数名 : CPlayer* Create( char* modelFileName )
// 引数   : char* modelFileName : モデルファイル名
// 戻り値 : CPlayer*型
// 説明   : 生成処理
//==============================================================================
CPlayer* CPlayer::Create(char* modelFileName)
{
	CPlayer* pNewInstance = new CPlayer();

	if (pNewInstance->Init(modelFileName) == false)
	{
		pNewInstance->Release();
		pNewInstance = NULL;
	}
	return pNewInstance;
}
