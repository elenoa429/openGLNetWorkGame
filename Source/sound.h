class CSound
{
	//---------------------------------------------
	// [ 列挙型 ]
	//---------------------------------------------
public:
	enum SOUND_LABEL
	{														/* ==サウンド識別子== */
		SOUND_LABEL_BGM_ST = 0,									// BGM開始地点
		SOUND_LABEL_BGM_TEST00 = SOUND_LABEL_BGM_ST,			// テスト用BGM
		SOUND_LABEL_BGM_END,									// BGM終了地点

		SOUND_LABEL_SE_ST = SOUND_LABEL_BGM_END,				// SE開始地点
		SOUND_LABEL_SE_TEST00 = SOUND_LABEL_SE_ST,				// テスト用SE
		SOUND_LABEL_SE_END,										// SE終了地点

		SOUND_LABEL_MAX = SOUND_LABEL_SE_END,					// 総数
	};

	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	// 再生処理
	virtual bool Play( SOUND_LABEL label ) = 0;

	// 停止処理
	virtual bool Stop( SOUND_LABEL label ) = 0;
	virtual bool Stop( void ) = 0;

	// 破棄処理
	virtual void Release( void ) = 0;

protected:
	// コンストラクタ
	CSound(){};
	CSound( const CSound& ){};

	// デストラクタ
	~CSound(){};

	// オペレーター
	void operator=( const CSound& ){};

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
protected:
	struct SOUND_PARAM
	{							/* ==サウンドパラメータ構造体== */
		char* pFileName;			// ファイル名
		int nLoopCnt;				// ループカウント
	};

	static const SOUND_PARAM m_soundParam[ SOUND_LABEL_MAX ];		// サウンドパラメータ

};

