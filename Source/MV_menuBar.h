class CMenuBar
{
	//---------------------------------------------
	// [ 列挙型 ]
	//---------------------------------------------
public:
	enum OPERATE_MENU
	{											/* ==メニュー操作識別子== */
		FILE_OPEN          = ID_40001,				// ファイルオープン
		FILE_CLOSE         = ID_40002,				// ファイルクローズ
		DESTROY_EXE        = ID_40011,				// exe終了
		CAMERA_DIAROG_OPEN = ID_40004,				// カメラダイアログの表示
		SET_LIGHT          = ID_40008,				// ライト切り替え
		SET_TEXTURE        = ID_40009,				// テクスチャ切り替え
		SET_MATERIAL       = ID_40015,				// マテリアル切り替え
		SET_BACKCOLOR      = ID_40010,				// 背景色設定
		HELP               = ID_40005,				// ヘルプ表示
		VERSION            = ID_40006,				// バージョン情報表示
		POLYGON_FILL       = ID_40012,				// 通常描画
		POLYGON_WIRE       = ID_40013,				// ワイヤーフレーム描画
		POLYGON_POINT      = ID_40014,				// 点描画
	};

	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	// コンストラクタ
	CMenuBar();

	// デストラクタ
	~CMenuBar(){};

	// メニューバー名生成
	bool CreateMenuName( WORD id );

	// メニューバーの処理実行
	bool OperateMenu( WPARAM wParam );

	// メニューバー名取得
	LPCSTR GetMenuName( void ){ return m_menuName; }

	// ハンドル関連
	HMENU GetHandle( void ){ return m_hMenu; }
	void SetHandle( HMENU handle ){ m_hMenu = handle; }

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
private:
	LPCSTR m_menuName;									// メニューバー名
	HMENU  m_hMenu;										// メニューバーハンドル
};

