class CEditorCameraGL : public CCameraGL
{
	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	// コンストラクタ
	CEditorCameraGL();

	// デストラクタ
	~CEditorCameraGL();

	// 初期化処理
	bool Init( VECTOR3 posV = VECTOR3( 0.0f , 1.0f , 1.0f ) , VECTOR3 posR = VECTOR3( 0.0f , 0.0f , 0.0f ) );

	// 終了処理
	void Uninit( void );

	// 更新処理
	void Update( void );

private:
	// 水平方向取得
	float GetHAngle( VECTOR3* vPosV , VECTOR3* vPosR );

	// 垂直方向取得
	float GetVAngle( VECTOR3* vPosV , VECTOR3* vPosR );

	// マウスの加速度から、角度移動量を生成
	VECTOR2 MoveRad( VECTOR3 vel );

	// マウスの加速度から、平行移動量を算出
	VECTOR3 MoveTranslate( VECTOR3 vel );

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
protected:
	float m_angleH;		// 水平方向
	float m_angleV;		// 垂直方向
};

