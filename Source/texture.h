class CTexture
{
	/* ===メンバ関数=== */
public:
	CTexture();																				// デフォルトコンストラクタ
	~CTexture();																			// デストラクタ

	virtual void Init( int width , int height ){ m_width = width; m_height = height; }		// 初期化処理
	virtual void Release( void ) = 0;														// 解放処理
	virtual void Set( void ) = 0;															// 設定処理

	/* ===メンバ変数=== */
protected:
	int m_width;					// 幅
	int m_height;					// 高さ
};

typedef class CTexture* LPTexture;

