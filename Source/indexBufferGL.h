//==============================================================================
// クラス宣言
//==============================================================================
class CIndexBufferGL : public CIndexBuffer
{
	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	// コンストラクタ
	CIndexBufferGL()
	{
		m_pData    = NULL;
		m_dataSize = 0;
	}

	// デストラクタ
	~CIndexBufferGL(){}

	// バッファー占有処理
	bool Lock( unsigned int offset , unsigned int size , void** ppData , unsigned long flags )
	{
		// エラーチェック
		if( m_pData == NULL )
		{
			return false;		// データ未設定のため処理失敗
		}

		// インデックスデータのバインド
		*ppData = m_pData;
		return true;			// 処理成功
	};

	// バッファー占有解除
	bool Unlock( void )
	{
		return true;			// 処理成功
	};

	// 解放処理
	void Release( void )
	{
		// インデックスデータの解放
		if( m_pData != NULL )
		{
			delete[] m_pData;
			m_pData = NULL;
		}

		// 自身の解放
		delete this;
	};

	// プライベートデータ取得
	bool GetPrivateData( void** ppData , DWORD* pSizeOfData )
	{
		// 頂点データ取得
		if( m_pData == NULL )
		{
			return false;		// データ未設定のため処理失敗
		}

		*ppData = m_pData;

		// データサイズ取得
		if( pSizeOfData != NULL )
		{
			*pSizeOfData = m_dataSize;
		}

		return true;			// 処理成功
	}

	// プライベートデータ設定
	bool SetPrivateData( void* pData , DWORD sizeOfData )
	{
		// エラーチェック
		if( pData == NULL )
		{
			return false;								// データ格納先ポインタがNULLのため処理失敗
		}

		// データ設定
		m_pData    = pData;
		m_dataSize = sizeOfData;

		return true;	// 処理成功
	}

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
protected:
	void* m_pData;			// 頂点インデックスデータ格納先へのポインタ
	DWORD m_dataSize;		// データサイズ
};

typedef class CIndexBufferGL* LPIndexBufferGL;

