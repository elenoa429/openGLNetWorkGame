class CNumber
{
	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	CNumber();
	~CNumber();

	void Release( void );
	virtual bool Init( int texDivW , int texDivH );
	virtual void Uninit( void ) = 0;
	virtual void Update( void ) = 0;
	virtual void Draw( void ) = 0;

	void SetNum( int value );

	int GetNum( void ){ return m_number; }
	
	void SetPos( VECTOR3 pos ){ m_pos = pos; }
	void SetRot( VECTOR3 rot ){ m_rot = rot; }
	void SetSize( VECTOR3 size ){ m_size = size; }
	void SetCol( COLOR_F32 col ){ m_col = col; }

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
protected:
	int m_number;				// 表示する数値( 0 〜 9 )
	
	VECTOR3 m_pos;				// 表示位置( 中心 )
	VECTOR3 m_rot;				// 回転角
	VECTOR3 m_size;				// 表示サイズ
	COLOR_F32 m_col;			// 表示色

	VECTOR2 m_tex;				// テクスチャ座標
	VECTOR2 m_texLen;			// テクスチャ長

	int m_texDivW;				// テクスチャ分割数( 横 )
	int m_texDivH;				// テクスチャ分割数( 縦 )


};

