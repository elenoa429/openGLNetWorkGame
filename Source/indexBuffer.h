class CIndexBuffer : public CBuffer
{
	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	// コンストラクタ
	CIndexBuffer(){}

	// デストラクタ
	~CIndexBuffer(){}

	// バッファー占有処理
	virtual bool Lock( unsigned int offset , unsigned int size , void** ppData , unsigned long flags ) = 0;

	// バッファー占有解除
	virtual bool Unlock( void ) = 0;

	// 解放処理
	virtual void Release( void ) = 0;

	// プライベートデータ取得
	virtual bool GetPrivateData( void** ppData , DWORD* pSizeOfData ) = 0;

	// プライベートデータ設定
	virtual bool SetPrivateData( void* pData , DWORD* pSizeOfData ) = 0;

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------

};

typedef class CIndexBuffer* LPIndexBuffer;

