class CSceneGL : public CScene
{
	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	// コンストラクタ
	CSceneGL(){};

	// デストラクタ
	~CSceneGL(){};

	// 初期化処理
	virtual bool Init( void ) { return true; }

	// 終了処理
	virtual void Uninit( void ) = 0;

	// 更新処理
	virtual void Update( void ) = 0;

	// 描画処理
	virtual void Draw( void ) = 0;

	// 位置関連
	virtual VECTOR3 GetPos( void ) = 0;
	virtual void SetPos( VECTOR3 pos ) = 0;

	// サイズ関連
	virtual VECTOR3 GetSize( void ) = 0;
	virtual void SetSize( VECTOR3 size ) = 0;

	// 角度関連
	virtual VECTOR3 GetRot( void ) = 0;
	virtual void SetRot( VECTOR3 rot ) = 0;

	// 色関連
	virtual COLOR_F32 GetCol( void ) = 0;
	virtual void SetCol( COLOR_F32 col ) = 0;

	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------

};

