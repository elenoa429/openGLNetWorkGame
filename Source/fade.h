class CFade
{
	//---------------------------------------------
	// [ 列挙型 ]
	//---------------------------------------------
public:
	enum FADE_STATE
	{						/* ==フェード状態識別子== */
		FADE_WAIT = 0,			// 待機状態
		FADE_IN,				// フェードイン
		FADE_OUT,				// フェードアウト
	};

	//---------------------------------------------
	// [ メンバ関数 ]
	//---------------------------------------------
public:
	static bool Init( char* texPath = NULL );
	static void Uninit( void );
	static void Update( void );
	static void Draw( void );

	static bool FadeStart( CMode* modeNext , int time = 60 );
	static FADE_STATE GetState( void ){ return m_state; }

	//---------------------------------------------
	// [ メンバ変数 ]
	//---------------------------------------------
private:
	static CMode* m_pNext;							// 次のモード

	static int m_fadeCnt;							// フェード用カウンタ
	static int m_fadeTime;							// フェードにかける時間

	static FADE_STATE m_state;						// 状態変数

	static COLOR_F32 m_col;							// 色
	static COLOR_F32 m_colMove;						// 色変化量

};

